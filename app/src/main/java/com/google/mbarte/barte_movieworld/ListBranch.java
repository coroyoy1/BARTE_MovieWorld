package com.google.mbarte.barte_movieworld;

public class ListBranch {

    private String head;
    private String desc;

    public ListBranch(String head, String desc) {
        this.head = head;
        this.desc = desc;
    }

    public String getHead() {
        return head;
    }

    public String getDesc() {
        return desc;
    }
}
